#include <iostream>
#include <cmath>

using namespace std;

int main ()
{
    int a = 2;

    cout << "sin(d)    :" << sin(a) << endl;
    cout << "abs(i)    :" << abs(a) << endl;
    cout << "floor(d)  :" << floor(a) << endl;
    cout << "sqrt(f)   :" << sqrt(a) << endl;
    cout << "pow(d, 2) :" << pow(a, 2) << endl;

    return 0;
}